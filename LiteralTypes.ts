
//String Literal types

let color: 'red'|'green'|'blue' 
color = 'red' //Valid
color = 'green' //Valid
color = 'blue' //Valid
// color = 'Yellow' //Invalid

console.log(color); //blue


//Boolean literal types
let isHappy: true
isHappy = true //Valid
// isHappy = false //Invalid
console.log(isHappy);


//Numeric Literal types
let marks:80|90|100
marks = 80 //Valid
marks = 90 //Valid
marks = 100 //Valid
//marks = 70 //Invalid
console.log(marks);


